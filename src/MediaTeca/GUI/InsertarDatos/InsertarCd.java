    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        String titulo = txtTitulo.getText();
        String artista = txtArtista.getText();
        String genero = txtGenero.getText();
        String h = txtHoras.getText();
        String m = txtMinutos.getText();
        String s = txtSegundos.getText();
        String numeroCanciones = txtNumeroCanciones.getText();
        String UnidadesDisp = txtUnidades.getText();
        int intNumeroCanciones = 0;
        int intUnidadesDisp = 0;
        boolean estado = false;
        boolean estado2 = false;

        if (!titulo.equals("") && !artista.equals("") && !genero.equals("") && !h.equals("") && !m.equals("") && !s.equals("") && !numeroCanciones.equals("") && !UnidadesDisp.equals("")) {
            String duracion = h + ":" + m + ":" + s;
            lblEstado.setText("");
            try {
                intNumeroCanciones = Integer.parseInt(numeroCanciones);
                estado = true;
                lblErrorNumero.setText("");

            } catch (NumberFormatException e) {
                estado = false;
                lblErrorNumero.setText("Este campo es num√©rico");
                e.printStackTrace(System.out);
            }
            try {
                intUnidadesDisp = Integer.parseInt(UnidadesDisp);
                estado2 = true;
                lblErrorUnidades.setText("");
            } catch (NumberFormatException e) {
                estado = false;
                lblErrorUnidades.setText("Este campo es num√©rico");
                e.printStackTrace(System.out);
            }

            if (estado == true && estado2 == true) {
                CdDAO cdDao = new CdDAO();
                Cd cd = new Cd(titulo, artista, genero, duracion, intNumeroCanciones, intUnidadesDisp); //lsdfklasdjf
                cdDao.insertar(cd);
                lblEstado.setText("Operacion realizada");
            }
        } else {
            lblEstado.setText("Los campos no pueden estar vacios");
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

