    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        String titulo = txtTitulo.getText();
        String editorial = txtEditorial.getText();
        String period = txtPeriod.getText();
        String dd = txtDD.getText();
        String mm = txtMM.getText();
        String yyyy = txtYY.getText();
        String unidades = txtUnidades.getText();
        String fecha = yyyy +"-"+mm+"-"+dd;
        int intUnidades = 0;
        boolean estado = false;

        if (!titulo.equals("") && !editorial.equals("") && !period.equals("") && !dd.equals("") && !mm.equals("") && !yyyy.equals("") && !unidades.equals("")) {
            lblEstado.setText("");
            try{
                intUnidades = Integer.parseInt(unidades);
                estado = true;
                lblErrorUnid.setText("");
                
            }catch(NumberFormatException e){
                estado = false;
                lblErrorUnid.setText("Este campo es num√©rico");
                e.printStackTrace(System.out);
            }
            
            if(estado){
                RevistaDAO revistaDao = new RevistaDAO();
                Revista revista = new Revista(titulo, editorial, period, fecha, intUnidades);
                revistaDao.insertar(revista);
                lblEstado.setText("Operacion realizada");
            }
            
            
        }else{
            lblEstado.setText("Los campos no pueden estar vacios");
        }

    }//GEN-LAST:event_btnAgregarActionPerformed

