    void llenarTablaSelect(){
        modelo = (DefaultTableModel) Tabla1.getModel();
        DvdDAO dvdDao = new DvdDAO();
        List<Dvd> dvds = dvdDao.seleccionar();
        Object[] lista = new Object[5];
        for(Dvd dvd: dvds){
            lista[0] = "DVD" + dvd.getId();
            lista[1] = dvd.getTitulo();
            lista[2] = dvd.getDirector();
            lista[3] = dvd.getDuracion();
            lista[4] = dvd.getGenero();
            modelo.addRow(lista);  
        }  
    }
