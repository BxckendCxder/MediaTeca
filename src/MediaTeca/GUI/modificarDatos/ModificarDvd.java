    void llenarTablaSelect(int codigo){
        modelo = (DefaultTableModel) Tabla1.getModel();
        DvdDAO dvdDao = new DvdDAO();
        List<Dvd> dvds = dvdDao.buscar(codigo);
        Object[] lista = new Object[5];
        for(Dvd dvd: dvds){
            lista[0] = "DVD" + dvd.getId();
            lista[1] = dvd.getTitulo();
            lista[2] = dvd.getDirector();
            lista[3] = dvd.getDuracion();
            lista[4] = dvd.getGenero();
            modelo.addRow(lista);  
        }  
    }
    
    void conversionObjeto(){
        
        Object obj1 = Tabla1.getValueAt(0, 0);
        Object obj2 = Tabla1.getValueAt(0, 1);
        Object obj3 = Tabla1.getValueAt(0, 2);
        Object obj4 = Tabla1.getValueAt(0, 3);
        Object obj5 = Tabla1.getValueAt(0, 4);
        String id1 = obj1.toString();
        id1 = id1.replace("DVD", "");
        int id = Integer.parseInt(id1);
        String titulo = obj2.toString();
        String director = obj3.toString();
        String duracion = obj4.toString();
        String genero = obj5.toString();
        Dvd dvd = new Dvd(id, titulo, director, duracion, genero);
        DvdDAO dvdDao = new DvdDAO();
        dvdDao.actualizar(dvd);
        JOptionPane.showMessageDialog(null, "Actualizado con exito");
    }
    
