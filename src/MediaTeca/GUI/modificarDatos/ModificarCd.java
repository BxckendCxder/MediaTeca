    void llenarTablaSelect(int codigo){
        modelo = (DefaultTableModel) Tabla1.getModel();
        CdDAO cdDao = new CdDAO();
        List<Cd> cds = cdDao.buscar(codigo);
        Object[] lista = new Object[8];
        for(Cd cd: cds){
            lista[0] = "CDA" + cd.getId();
            lista[1] = cd.getTitulo();
            lista[2] = cd.getArtista();
            lista[3] = cd.getGenero();
            lista[4] = cd.getDuracion();
            lista[5] = cd.getNumeroCanciones();
            lista[6] = cd.getUnidadesDisponibles();
            modelo.addRow(lista);  
        }  
    }
    
    void ConversionObjeto(){
        
        Object obj1 = Tabla1.getValueAt(0, 0);
        Object obj2 = Tabla1.getValueAt(0, 1);
        Object obj3 = Tabla1.getValueAt(0, 2);
        Object obj4 = Tabla1.getValueAt(0, 3);
        Object obj5 = Tabla1.getValueAt(0, 4);
        Object obj6 = Tabla1.getValueAt(0, 5);
        Object obj7 = Tabla1.getValueAt(0, 6);
        String id1 = obj1.toString();
        id1 = id1.replace("CDA", "");
        int id = Integer.parseInt(id1);
        String titulo = obj2.toString();
        String artista = obj3.toString();
        String genero = obj4.toString();
        String duracion = obj5.toString();
        int canciones = Integer.parseInt(obj6.toString());
        int unidades = Integer.parseInt(obj7.toString());
        Cd cd = new Cd(id, titulo, artista, genero, duracion, canciones, unidades);
        CdDAO cdDao = new CdDAO();
        cdDao.actualizar(cd);
        JOptionPane.showMessageDialog(null, "Actualizado con exito");

    }
    
